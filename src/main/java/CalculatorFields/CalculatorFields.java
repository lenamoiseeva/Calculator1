package CalculatorFields;

public abstract class CalculatorFields {
    private int fn;
    private int sn;

    public CalculatorFields(){}

    public int getFn() {
        return fn;
    }
    public void setFn(int fn) {
        this.fn = fn;
    }
    public int getSn() {
        return sn;
    }
    public void setSn(int sn) {
        this.sn = sn;
    }
}
